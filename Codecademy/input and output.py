# using the 'input' function

name = input('What is your name?')

# generic message

print("Very nice to meet you, person!")

# String interpolation using f strings

print(f"Very nice to meet you, {name}!")

# Multiple variables in the print statement

print("It's very nice to meet you", name, "!")

# String concatenation using '+'

print("It's very nice to meet you, " + name + "!")

# using str.format

print('It\'s very nice to meet you, {}!'.format(name))





